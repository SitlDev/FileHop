// в”Җв”Җв”Җ Try to match a Printify product title to a catalog entry в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
function matchCatalogId(printifyTitle) {
  const titleUpper = printifyTitle.toUpperCase();
  for (const [id, slogan] of Object.entries(CATALOG_NAMES)) {
    if (titleUpper.includes(slogan) || slogan.includes(titleUpper)) {
      return parseInt(id);
    }
  }
  return null;
}

// в”Җв”Җв”Җ Parse variants to extract size-to-ID mapping в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
function extractSizeVariants(variants) {
  const sizes = ['S', 'M', 'L', 'XL', '2XL', 'XXL'];
  const result = {};
  for (const v of variants) {
    const titleUpper = (v.title || '').toUpperCase();
    for (const size of sizes) {
      const key = size === 'XXL' ? '2XL' : size;
      if (titleUpper.includes(`/ ${size}`) || titleUpper.startsWith(size) || titleUpper.includes(` ${size}`) || titleUpper === size) {
        if (!result[key]) result[key] = v.id;
      }
    }
  }
  return result;
}

// в”Җв”Җв”Җ Generate catalog.js content в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
function generateCatalogJs(mapping) {
  const entries = Object.entries(CATALOG_NAMES).map(([id, slogan]) => {
    const match = mapping[parseInt(id)];
    const printifyProductId = match ? `'${match.printifyProductId}'` : `'FILL_ME'`;
    const variants = match ? match.variants : {};
    const variantsStr = ['S', 'M', 'L', 'XL', '2XL'].map(s => {
      const variantId = variants[s] ?? null;
      return `    ${s}: ${variantId}`;
    }).join(',\n');

    return `  ${id}: {
    slogan: '${slogan}',
    printifyProductId: ${printifyProductId},
    variants: {
${variantsStr},
    },
  }`;
  });

  return `/**
 * lib/catalog.js вҖ” AUTO-GENERATED by scripts/setup.js on ${new Date().toISOString()}
 * Maps RebelThreads shirt IDs вҶ’ Printify product + variant lookup.
 * Re-run "node scripts/setup.js" to refresh.
 */

export const CATALOG = {
${entries.join(',\n')},
};

export function resolveVariant(shirtId, size) {
  const product = CATALOG[shirtId];
  if (!product) throw new Error(\`Unknown shirt ID: \${shirtId}\`);
  const variantId = product.variants[size];
  if (!variantId) throw new Error(\`Variant not configured for shirt \${shirtId} / size \${size}.\`);
  return { printifyProductId: product.printifyProductId, variantId };
}
`;
}

// в”Җв”Җв”Җ Register webhooks в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
async function registerWebhooks(shopId) {
  const publicUrl = process.env.PUBLIC_URL;
  if (!publicUrl || publicUrl.includes('localhost')) {
    console.log('вҡ пёҸ   Skipping webhooks вҖ” PUBLIC_URL not set to a live domain.');
    console.log('    Set PUBLIC_URL=https://your-api-domain.com in .env to enable.\n');
    return;
  }

  const events = ['order:created', 'order:sent-to-production', 'shipment:created', 'shipment:delivered'];
  const endpointBase = `${publicUrl}/webhooks/printify`;

  for (const topic of events) {
    try {
      // Check if already registered
      const existing = await printify(`/shops/${shopId}/webhooks.json`);
      const alreadySet = (existing.data || existing).some(w => w.topic === topic);
      if (alreadySet) {
        console.log(`  вң“  Webhook already registered: ${topic}`);
        continue;
      }
      await fetch(`https://api.printify.com/v1/shops/${shopId}/webhooks.json`, {
        method: 'POST',
        headers: {
          Authorization: `Bearer ${process.env.PRINTIFY_API_KEY}`,
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({ topic, url: endpointBase }),
      });
      console.log(`  вң…  Registered webhook: ${topic}`);
    } catch (e) {
      console.log(`  вқҢ  Failed to register ${topic}: ${e.message}`);
    }
  }
}

