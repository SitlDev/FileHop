#!/usr/bin/env node
/**
 * RebelThreads вҖ” Printify Setup & Sync Script
 * 
 * Run from the backend directory:
 *   node scripts/setup.js
 * 
 * No npm install needed вҖ” uses Node 18+ built-in fetch.
 * Reads credentials directly from backend/.env
 * 
 * What it does:
 *   1. Verifies your Printify API key & confirms your Shop ID
 *   2. Lists all products with their variant IDs
 *   3. Auto-matches your Printify products to the RebelThreads catalog
 *   4. Writes the updated catalog.js file automatically
 *   5. Registers Printify webhooks (if PUBLIC_URL is set)
 */

import { readFileSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');

// в”Җв”Җв”Җ Load .env manually (no dotenv needed) в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
function loadEnv() {
  const envPath = join(ROOT, '.env');
  try {
    const raw = readFileSync(envPath, 'utf8');
    raw.split('\n').forEach(line => {
      const trimmed = line.trim();
      if (!trimmed || trimmed.startsWith('#')) return;
      const [key, ...rest] = trimmed.split('=');
      if (key && rest.length) process.env[key.trim()] = rest.join('=').trim();
    });
  } catch {
    console.error('вқҢ  Could not read backend/.env вҖ” make sure it exists!');
    process.exit(1);
  }
}

// в”Җв”Җв”Җ Printify API helper в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
async function printify(path) {
  const apiKey = process.env.PRINTIFY_API_KEY;
  if (!apiKey) throw new Error('PRINTIFY_API_KEY not set in .env');
  const res = await fetch(`https://api.printify.com/v1${path}`, {
    headers: { Authorization: `Bearer ${apiKey}` }
  });
  const data = await res.json();
  if (!res.ok) throw new Error(data.message || JSON.stringify(data));
  return data;
}

// в”Җв”Җв”Җ The slogan names in our catalog, by ID в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
